package zoo.animal;

/**
Represents the gender of an animal.
<p>
This enum consists of three possible genders: Male, Female, and Unknown.
</p>
@author richardboamah
*
*/

public enum Gender {
	/*
	* Represents the male gender of an animal.
	*/
	MALE,
	
	/**
	 * Represents the female gender of an animal.
	 */
	FEMALE,
	
	/**
	 * Represents the gender of an animal that is unknown or not declared.
	 */
	UNKNOWN,
	
}
